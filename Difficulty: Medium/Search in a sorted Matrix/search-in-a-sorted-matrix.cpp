class Solution {
  public:
    // Function to search a given number in row-column sorted matrix.
    bool searchMatrix(vector<vector<int>> &mat, int x) {
        // code here
        int r = mat.size(), c = mat[0].size();
        int j = r*c-1, i = 0;
        while(i <= j){
            int m = (i+j)/2;
            int ele = mat[m/c][m%c];
            if(ele == x) return true;
            else if(ele < x) i = m+1;
            else j = m-1;
        }
        return false;
    }
};

