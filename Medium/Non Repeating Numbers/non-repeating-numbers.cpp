class Solution
{
public:
    vector<int> singleNumber(vector<int> nums) 
    {
        // Code here.
        int ans = 0;
        for(int x : nums) ans ^= x;
        ans = ans & ~(ans-1);
        int a=0, b=0;
        for(int x : nums){
            if(ans & x) a ^= x;
            else b ^= x;
        }
        if(a < b) return {a, b};
        return {b, a};
    }
};

