class Solution
{
    public:
    //Function to find length of longest increasing subsequence.
    int longestSubsequence(int n, int a[])
    {
       // your code here
       int len=0;
       for(int j=0; j<n; j++){
           if(len == 0 || a[len-1] < a[j]) a[len++] = a[j];
           else{
               int ind = lower_bound(a, a+len, a[j]) - a;
               a[ind] = a[j];
           }
       }
       return len;
    }
};

